#include <stdio.h>
void main()
{
    float number_one,number_two;
    printf("Enter two numbers: ");
    fflush(stdin); fflush(stdout);
    scanf("%f %f",&number_one,&number_two);
    printf("Product: %f",number_one * number_two);

}
